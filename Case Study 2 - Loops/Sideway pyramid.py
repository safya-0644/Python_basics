# Upward Triangle
for i in range(1, 5):  # Loop through numbers 1 to 4 to print the upward triangle
  print(' '.join(['*'] * i))  # Print i asterisks with a space in between, forming a triangle

# Downward Triangle
for i in range(5, 0, -1):  # Loop through numbers 5 to 1 to print the downward triangle
  print(' '.join(['*'] * i))  # Print i asterisks with a space in between, forming the opposite triangle
